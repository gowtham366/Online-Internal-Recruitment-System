package com.recruitmentSystem.bean;

public class EmployeeBean {
	private String empId;
	private String empName;
	private String empSkill;
	private String empDomain;
	private int empExp;
	private String empProjectId;
	private String empReqId;
	
	/**
	 * @return the empId
	 */
	public String getEmpId() {
		return empId;
	}
	/**
	 * @param empId the empId to set
	 */
	public void setEmpId(String empId) {
		this.empId = empId;
	}
	/**
	 * @return the empName
	 */
	public String getEmpName() {
		return empName;
	}
	/**
	 * @param empName the empName to set
	 */
	public void setEmpName(String empName) {
		this.empName = empName;
	}
	/**
	 * @return the empSkill
	 */
	public String getEmpSkill() {
		return empSkill;
	}
	/**
	 * @param empSkill the empSkill to set
	 */
	public void setEmpSkill(String empSkill) {
		this.empSkill = empSkill;
	}
	/**
	 * @return the empDomain
	 */
	public String getEmpDomain() {
		return empDomain;
	}
	/**
	 * @param empDomain the empDomain to set
	 */
	public void setEmpDomain(String empDomain) {
		this.empDomain = empDomain;
	}
	/**
	 * @return the empExp
	 */
	public int getEmpExp() {
		return empExp;
	}
	/**
	 * @param empExp the empExp to set
	 */
	public void setEmpExp(int empExp) {
		this.empExp = empExp;
	}
	/**
	 * @return the empProjectId
	 */
	public String getEmpProjectId() {
		return empProjectId;
	}
	/**
	 * @param empProjectId the empProjectId to set
	 */
	public void setEmpProjectId(String empProjectId) {
		this.empProjectId = empProjectId;
	}
	/**
	 * @return the empRmId
	 */
	public String getEmpReqId() {
		return empReqId;
	}
	/**
	 * @param empRmId the empRmId to set
	 */
	public void setEmpReqId(String empReqId) {
		this.empReqId = empReqId;
	}
	
	
}
